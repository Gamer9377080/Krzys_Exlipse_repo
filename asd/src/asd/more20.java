package asd;

public class more20 {
	public static boolean morE20(int n) {
		if((n%20==1)||(n%20==2)){
			return true;
		}else {
			return false;
		}
}
	
		
		
		
	public static void main(String[] args) {
		System.out.println(morE20(20));
		System.out.println(morE20(21));
		System.out.println(morE20(22));
	}
}

